package com.github.lemfi.adventofcode.year2020

import kotlin.math.max

fun day3_1(data: String) = data.lines()
    .map { it.toCharArray().map { it != '.' } }
    .drop(1)
    .fold(0 to 0) {
            res, line ->
        line.let {
            (1..max((res.second+3)/line.size, 1) + 1).map { line }.flatten()
        }
            .let { expandedline ->
                expandedline[res.second+3].let { if (it) res.first + 1 to res.second + 3 else res.first to res.second + 3 }
            }

    }.first

fun day3_2(data: String) = data.lines()
    .map { it.toCharArray().map { it != '.' } }
    .let {
            trees ->
        listOf(1 to 1, 3 to 1, 5 to 1, 7 to 1, 1 to 2) .map { slope ->
            trees.drop(slope.second)
                .filterIndexed { index, _ -> index % slope.second == 0 }
                .fold(0 to 0) {
                        res, line ->
                    line.let {
                        (1..max((res.second + slope.first)/line.size, 1) + 1).map { line }.flatten()
                    }
                        .let { expandedline ->
                            expandedline[res.second + slope.first]
                                .let {
                                    if (it) res.first + 1 to res.second + slope.first else res.first to res.second + slope.first
                                }
                        }

                }.first

        }
    }.reduce {
            val1, val2 -> val1 * val2
    }

